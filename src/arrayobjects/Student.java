public class Student 
{    
    private String name;
    private int age;

    public String getName() 
    {
        return name;
    }//ends getName

    public void setName(String name) 
    {
        this.name = name;
    }//ends setName

    public int getAge() 
    {
        return age;
    }//ends getAge

    public void setAge(int age) 
    {
        this.age = age;
    }//ends setAge
       
}//ends class Student
